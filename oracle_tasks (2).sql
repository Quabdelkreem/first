DECLARE
  e EXCEPTION;
  PRAGMA EXCEPTION_INIT(e, -942);
BEGIN
  EXECUTE IMMEDIATE 'DROP TABLE EMP_TEST PURGE';
EXCEPTION WHEN e THEN NULL;
END;
/
BEGIN
  EXECUTE IMMEDIATE 'DROP TABLE PRODUCT PURGE';
EXCEPTION WHEN OTHERS THEN
  IF SQLCODE != -942 THEN RAISE; END IF;
END;
/
BEGIN
  EXECUTE IMMEDIATE 'DROP TABLE STUDENTS PURGE';
EXCEPTION WHEN OTHERS THEN
  IF SQLCODE != -942 THEN RAISE; END IF;
END;
/
CREATE TABLE EMP_TEST ( NAME VARCHAR2(100) );
INSERT INTO EMP_TEST (NAME) VALUES ('   Alice   ');
INSERT INTO EMP_TEST (NAME) VALUES ('  Bob');
INSERT INTO EMP_TEST (NAME) VALUES ('Charlie   ');
INSERT INTO EMP_TEST (NAME) VALUES ('   *Diana*   ');
COMMIT;
SELECT NAME, TRIM(NAME) FROM EMP_TEST;
SELECT NAME, LTRIM(NAME) FROM EMP_TEST;
SELECT NAME, RTRIM(NAME) FROM EMP_TEST;
SELECT NAME, TRIM('*' FROM TRIM(NAME)) FROM EMP_TEST;
SELECT REPLACE('database','a','@') FROM dual;
SELECT REPLACE('This is the old version','old','new') FROM dual;
