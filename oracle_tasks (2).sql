SELECT TO_CHAR(3500,'L9,999.00') FROM dual;
CREATE TABLE STUDENTS ( NAME VARCHAR2(100) NOT NULL, MARKS NUMBER(5,2) NOT NULL );
INSERT INTO STUDENTS (NAME, MARKS) VALUES ('Ahmed',95);
INSERT INTO STUDENTS (NAME, MARKS) VALUES ('Mona',82);
INSERT INTO STUDENTS (NAME, MARKS) VALUES ('Khaled',76);
INSERT INTO STUDENTS (NAME, MARKS) VALUES ('Sara',68);
INSERT INTO STUDENTS (NAME, MARKS) VALUES ('Laila',89);
COMMIT;
SELECT NAME, MARKS,
       CASE
         WHEN MARKS >= 90 THEN 'A'
         WHEN MARKS BETWEEN 80 AND 89.9999 THEN 'B'
         WHEN MARKS BETWEEN 70 AND 79.9999 THEN 'C'
         ELSE 'F'
       END
FROM STUDENTS
ORDER BY MARKS DESC;
